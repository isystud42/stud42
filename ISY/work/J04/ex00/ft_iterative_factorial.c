int	ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (i < 0)
	{
		return (0);
	}
	while (i > 1)
	{
		nb = nb * (i-1);
		i += -1 ;
		printf("%d" , nb);
	}
	return (nb);
}
