#include <stdlib.h>

void	ft_ultimate_div_mod(int *a, int *b);
{
	int tema;
	int temb;
	tmpb = *b;
	tmpa = *a;
	*a = tmpa / tmpb;
	*b = tmpa % tmpb;
	return ;
}
