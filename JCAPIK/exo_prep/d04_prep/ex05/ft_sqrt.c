int ft_sqrt(int nb)
{
	int i;

	i = nb * 5;
	if ( i % 10 == 5)
	{
		i += 5;
	}
	i = i / 10;

	while (i > 0)
	{
		if ( i * i == nb)
		{
			return (i);
		}
		i--;
	}
	return (0);
}
