#include <unistd.h>


int  ft_putchar( char c)
{
	write(1, &c, 1);
	return (0);
}

int main (int argc, char *argv[])
{
	int C = 97;
	while (C <= 122)
	{
		ft_putchar(C);
		C++;
	}
	return (0);
} 
